package Ejercicios5;

public class ejercicio3 {
    public static void main(String[] args) {
        int i = 0;
    
            // Usare un bucle do-while para iterar del 0 al 100
        do {
            if (i % 5 == 0) {
                System.out.println(i);
            }
            i++;
        } while (i <= 100);
    }
}
    